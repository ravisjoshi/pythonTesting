def hourGlassMaxSum(arr):
    count_list = []
    row = len(arr[0])
    column = len(arr)
    for x in range(row-2):
        for y in range(column-2):
            res = sum(arr[x][y:y+3]) + arr[x+1][y+1] + sum(arr[x+2][y:y+3])
            count_list.append(res)
    return max(count_list)

